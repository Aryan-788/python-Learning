P = int(input("Please enter value for P:  "))
Q = int(input("Please enter value for Q:  "))
print("****Before Swapping****")
print("The value of P is ",P)
print("The value of Q is ",Q)

P, Q = Q, P
print("****After Swapping****")
print("The value of P is ",P)
print("The value of Q is ",Q)
